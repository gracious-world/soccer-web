            <p>第一章 总则</p>
            <p>第一条：中国体育彩票竞彩足球游戏是以足球比赛为彩票游戏媒介，由彩民选择竞猜场次并预测比赛结果，以实际比赛结果为彩票兑奖依据的一种体育彩票游戏玩法。中国体育彩票竞彩足球游戏规则遵守财政部《彩票发行与销售管理暂行规定》（财综[2002]13号）的有关规定，由国家体育总局体育彩票管理中心统一发行。</p>
            <p>第二条：“竞彩足球游戏”实行自愿购买，凡购买该彩票者即被视为同意并遵守本规则。</p>
            <p>第三条：竞彩足球游戏以国家体育总局体育彩票管理中心选定的国际重要足球比赛为竞猜对象，由购票人对指定的比赛场次在全场90分钟（含伤停补时）的比赛结果进行投注。</p>
            <p>第二章 投注方式</p>
            <p>第一条：投注方式包括单式投注和复式投注。其中，对所选比赛场次的比赛结果均选择1种投注结果为单式投注。对于某个或某几个比赛场次选择2种或2种以上的投注结果为复式投注。</p>
            <p>第二条：购票人选择1场比赛进行投注的为“单场投注”；选择2场或2场以上比赛进行串联投注的为“过关投注”。在过关投注中，所选比赛的结果全部竞猜正确才能中奖。所选比赛场次的过关投注方式由国家体育总局体育彩票管理中心选定，根据不同比赛场次情况，提供下表中全部或部分过关投注方式。</p>
            <p class="f_center"><img style="MARGIN: 0px; FLOAT: none" alt="竞彩足球玩法介绍" src="http://img1.cache.netease.com/catchpic/2/2C/2CB66461C953EADCA9B0135E8BC6F94B.gif"></p>
            <p>第三条：“竞彩足球游戏”每注2元，不返还本金。根据投注额、突发事件等因素，销售系统可能会拒绝某些大额投注，暂停或提前截止针对某场比赛的某些特定过关组合、特定结果选项的投注，暂停或提前截止针对某场比赛的所有投注。未满18周岁的未成年人不得投注！</p><b style="mso-bidi-font-weight: normal"><p class="f_center"><img alt="竞彩足球玩法介绍" src="http://img3.cache.netease.com/help/2012/3/16/2012031617091729125.jpg"><br>&nbsp;</p>
